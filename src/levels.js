const generateLevels = () => {
  const layouts = generateLayouts()
  return layouts.map(layout => makeLevel(layout))
}

const makeLevel = layout => {
  return {
    layout: layout.map((row, y) => row.map((type, x) => ({ type, x, y }))),
    enemies: []
  }
}

